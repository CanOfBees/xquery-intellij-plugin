package uk.co.reecedunn.intellij.plugin.core.tests.psi;

import com.intellij.mock.MockPsiDocumentManager;
import com.intellij.openapi.editor.Document;
import com.intellij.openapi.fileEditor.FileDocumentManager;
import com.intellij.openapi.vfs.VirtualFile;
import com.intellij.psi.PsiFile;
import org.jetbrains.annotations.NotNull;
import org.jetbrains.annotations.Nullable;

public class MockPsiDocumentManagerEx extends MockPsiDocumentManager {
    @Override
    @Nullable
    public Document getDocument(@NotNull PsiFile file) {
        VirtualFile vFile = file.getViewProvider().getVirtualFile();
        return FileDocumentManager.getInstance().getDocument(vFile);
    }
}
